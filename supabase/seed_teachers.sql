-- إضافة سجلات حضور لآخر أسبوع
INSERT INTO teacher_attendance_records (teacher_id, date, status, check_in_time, check_out_time, late_minutes)
SELECT 
    t.teacher_id,
    d.date,
    CASE 
        WHEN t.employment_status = 'إجازة' THEN 'إجازة'
        WHEN random() < 0.85 THEN 'حاضر'
        WHEN random() < 0.5 THEN 'متأخر'
        ELSE 'غائب'
    END as status,
    CASE WHEN t.employment_status != 'إجازة' AND random() < 0.85 THEN '07:30:00' ELSE NULL END,
    CASE WHEN t.employment_status != 'إجازة' AND random() < 0.85 THEN '14:30:00' ELSE NULL END,
    CASE WHEN random() < 0.15 THEN floor(random() * 30)::int ELSE 0 END
FROM teachers t
CROSS JOIN (
    SELECT generate_series(
        CURRENT_DATE - INTERVAL '7 days',
        CURRENT_DATE,
        INTERVAL '1 day'
    )::date as date
) d
WHERE EXTRACT(DOW FROM d.date) NOT IN (5, 6); -- استثناء الجمعة والسبت

